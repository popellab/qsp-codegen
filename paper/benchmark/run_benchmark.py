#!/usr/bin/env python3
"""Reproducible benchmark for qsp-codegen vs MATLAB SimBiology.

Times the same SBML model integrated by

  1. SimBiology's ``sbiosimulate`` (sundials backend, matched tolerances), and
  2. the C++ ``qsp_sim`` binary generated by ``qsp-codegen``.

Two regimes are reported:

  * **Integration only.**  MATLAB ``tic/toc`` around ``sbiosimulate`` (one
    persistent MATLAB session, ``--reps`` iterations after a warm-up call).
    For C++, wall-clock per ``qsp_sim`` invocation minus a calibrated
    process-startup baseline (the binary always pays ~one CRT init per
    call). This isolates ODE-solver work from process-launch overhead.

  * **Wall-clock per invocation.**  One full ``matlab -batch`` launch that
    loads the model and simulates once; one ``qsp_sim`` invocation. This
    is what an SBI workflow pays per call when it is *not* using a
    persistent MATLAB worker pool.

The script does not time codegen or the C++ build — both are one-time
setup costs that an SBI workflow amortises across millions of calls.
``--qsp-sim`` must point at an already-built binary.

Outputs ``results.csv`` (raw timings) and ``results_table.md``
(formatted Markdown table suitable for pasting into ``paper.md``).

Example::

    matlab -batch "run('paper/benchmark/build_model.m')"
    cmake -S cpp -B cpp/build && cmake --build cpp/build --target qsp_sim
    python paper/benchmark/run_benchmark.py \\
        --qsp-sim cpp/build/qsp_sim --reps 20
"""
from __future__ import annotations

import argparse
import shutil
import statistics
import subprocess
import sys
import time
from pathlib import Path

import numpy as np

HERE = Path(__file__).resolve().parent


def run(cmd, **kw):
    """subprocess.run with check=True and inherited stdio for diagnostics."""
    return subprocess.run(cmd, check=True, **kw)


def time_subprocess(cmd, reps, **kw):
    """Run ``cmd`` ``reps`` times, returning the per-call wall-clock seconds."""
    out = []
    for _ in range(reps):
        t0 = time.perf_counter()
        run(cmd, **kw)
        out.append(time.perf_counter() - t0)
    return out


def calibrate_cpp_startup(qsp_sim, reps=5):
    """Median wall-clock cost of `qsp_sim` invocation that does no integration.

    Run with no args -- the binary prints usage and exits non-zero.  We
    capture stdio and ignore the return code; the relevant signal is just
    the process launch overhead.
    """
    samples = []
    for _ in range(reps):
        t0 = time.perf_counter()
        subprocess.run(
            [str(qsp_sim)], capture_output=True, check=False,
        )
        samples.append(time.perf_counter() - t0)
    return statistics.median(samples)


CONSUMER_CMAKE = '''cmake_minimum_required(VERSION 3.16)
project(qsp_sim_bench CXX)
set(CMAKE_CXX_STANDARD 17)
if(NOT CMAKE_BUILD_TYPE)
    set(CMAKE_BUILD_TYPE Release)
endif()

execute_process(
    COMMAND "${PYTHON_EXECUTABLE}" -m qsp_codegen.cmake --prefix
    OUTPUT_VARIABLE QSP_PREFIX
    OUTPUT_STRIP_TRAILING_WHITESPACE
    COMMAND_ERROR_IS_FATAL ANY)
list(APPEND CMAKE_PREFIX_PATH "${QSP_PREFIX}")
find_package(qsp_sim_core CONFIG REQUIRED)

add_executable(qsp_sim
    ${QSP_SIM_CORE_DRIVER_SOURCE}
    ${CMAKE_CURRENT_SOURCE_DIR}/ODE_system.cpp
    ${CMAKE_CURRENT_SOURCE_DIR}/QSPParam.cpp)
target_include_directories(qsp_sim PRIVATE ${CMAKE_CURRENT_SOURCE_DIR})
target_link_libraries(qsp_sim PRIVATE qsp_sim_core::qsp_sim_core)
'''


def build_qsp_sim(gen_dir, build_dir):
    """Configure + build a `qsp_sim` executable from generated sources.

    This is a one-time *setup* step — the timing regimes below exclude
    it explicitly. The CMakeLists.txt is generated next to the codegen
    outputs so the same sources/builds are reproducible.
    """
    cml = gen_dir / "CMakeLists.txt"
    cml.write_text(CONSUMER_CMAKE)

    if build_dir.exists():
        shutil.rmtree(build_dir)
    build_dir.mkdir(parents=True)

    print(f"[setup] cmake configure (qsp_sim consumer build)...")
    run([
        "cmake", "-S", str(gen_dir), "-B", str(build_dir),
        f"-DPYTHON_EXECUTABLE={sys.executable}",
        "-DCMAKE_BUILD_TYPE=Release",
    ])
    print(f"[setup] cmake build qsp_sim...")
    run(["cmake", "--build", str(build_dir),
         "--target", "qsp_sim", "-j"])
    bin_path = build_dir / "qsp_sim"
    if not bin_path.exists():
        sys.exit(f"qsp_sim not built at expected path: {bin_path}")
    return bin_path


def build_param_xml(snippet_path, out_path):
    """Wrap the codegen-emitted <QSP> snippet in a <Param> document.

    qsp_sim's ParamBase looks up parameters by Boost property_tree paths
    rooted at the document's top-level element name (e.g.,
    ``Param.QSP.init_value.Parameter.k12``), so the wrapper must be
    named ``Param``.
    """
    snippet = snippet_path.read_text().rstrip()
    out_path.write_text(f"<Param>\n{snippet}\n</Param>\n")


def fmt_seconds(samples):
    """Median + (p25, p75) interval, formatted for the markdown table."""
    arr = np.asarray(samples, dtype=float)
    return float(np.median(arr)), float(np.percentile(arr, 25)), \
        float(np.percentile(arr, 75))


def write_table(results, out_md):
    rows = []
    rows.append("| Mode | MATLAB SimBiology (s) | qsp-codegen / CVODE (s) | Speedup |")
    rows.append("|---|---|---|---|")

    for label, m_samples, c_samples in results:
        m_med, m_lo, m_hi = fmt_seconds(m_samples)
        c_med, c_lo, c_hi = fmt_seconds(c_samples)
        speedup = m_med / c_med if c_med > 0 else float("inf")
        rows.append(
            f"| {label} | {m_med:.3f} ({m_lo:.3f}–{m_hi:.3f}) | "
            f"{c_med:.4f} ({c_lo:.4f}–{c_hi:.4f}) | "
            f"{speedup:.1f}× |"
        )
    out_md.write_text("\n".join(rows) + "\n")


def write_csv(records, out_csv):
    """Wide CSV: one column per (regime, side), one row per repetition."""
    max_len = max(len(v) for v in records.values())
    cols = list(records.keys())
    with out_csv.open("w") as f:
        f.write(",".join(cols) + "\n")
        for i in range(max_len):
            row = [
                f"{records[c][i]:.6f}" if i < len(records[c]) else ""
                for c in cols
            ]
            f.write(",".join(row) + "\n")


def main():
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--sbml", type=Path, default=HERE / "model.sbml",
                   help="SBML file to benchmark (default: ./model.sbml)")
    p.add_argument("--qsp-sim", type=Path, default=None,
                   help="Path to a pre-built qsp_sim binary. If omitted, "
                        "the script generates+builds one from the SBML as "
                        "an untimed setup step.")
    p.add_argument("--matlab", default="matlab",
                   help="MATLAB binary (default: 'matlab')")
    p.add_argument("--reps", type=int, default=20,
                   help="Repetitions per timing regime (default: 20)")
    p.add_argument("--cold-reps", type=int, default=3,
                   help="Wall-clock invocations per side (default: 3, since "
                        "MATLAB cold start is ~6s).")
    p.add_argument("--t-end", type=float, default=365.0,
                   help="Integration end time, days (default: 365)")
    p.add_argument("--dt", type=float, default=0.1,
                   help="Output spacing, days. MATLAB uses this as a fixed "
                        "OutputTimes grid; qsp_sim uses it as a CV_ONE_STEP "
                        "cadence floor (passed as --min-cadence-hours = 24*dt). "
                        "Default: 0.1 (= 2.4 h floor on the C++ side).")
    p.add_argument("--work", type=Path, default=HERE / "_work",
                   help="Scratch directory (default: ./_work)")
    p.add_argument("--out", type=Path, default=HERE / "results.csv",
                   help="Per-rep timings CSV (default: ./results.csv)")
    p.add_argument("--out-table", type=Path, default=HERE / "results_table.md",
                   help="Markdown table (default: ./results_table.md)")
    p.add_argument("--keep-work", action="store_true",
                   help="Don't wipe scratch dir on entry.")
    p.add_argument("--parity-rtol", type=float, default=5e-3,
                   help="Relative tolerance for the post-timing parity "
                        "check (default: 5e-3).")
    p.add_argument("--parity-atol", type=float, default=1e-6,
                   help="Absolute tolerance floor for parity. Below this "
                        "value, species are considered numerically zero on "
                        "both sides (default: 1e-6).")
    args = p.parse_args()

    if not args.sbml.exists():
        sys.exit(f"SBML not found: {args.sbml}\n"
                 f"Run `matlab -batch \"run('{HERE}/build_model.m')\"` first.")
    if args.qsp_sim is not None and not args.qsp_sim.exists():
        sys.exit(f"qsp_sim binary not found: {args.qsp_sim}")

    # 1. Set up scratch dir + run codegen + write the param XML.
    if args.work.exists() and not args.keep_work:
        shutil.rmtree(args.work)
    args.work.mkdir(parents=True, exist_ok=True)
    cpp_gen = args.work / "cpp_gen"
    cpp_gen.mkdir(exist_ok=True)

    qsp_codegen_bin = shutil.which("qsp-codegen")
    if qsp_codegen_bin is None:
        sys.exit("qsp-codegen CLI not found on PATH. Install with "
                 "`uv pip install -e .` from the repo root.")
    print(f"[setup] qsp-codegen --sbml {args.sbml} --out-dir {cpp_gen}")
    run([qsp_codegen_bin, "--sbml", str(args.sbml), "--out-dir", str(cpp_gen)])

    snippet = cpp_gen / "qsp_params_xml_snippet.xml"
    if not snippet.exists():
        sys.exit(f"codegen did not emit snippet: {snippet}")
    param_xml = args.work / "param_all.xml"
    build_param_xml(snippet, param_xml)
    print(f"[setup] wrote {param_xml}")

    qsp_sim = args.qsp_sim
    if qsp_sim is None:
        qsp_sim = build_qsp_sim(cpp_gen, args.work / "cpp_build")
    print(f"[setup] qsp_sim: {qsp_sim}")

    # 2. Integration-only timings (default IC, no doses).
    def matlab_inproc_run(label, apply_doses, times_csv, load_csv, traj_csv):
        print(f"\n[matlab in-process / {label}] {args.reps} reps...")
        cmd_parts = [
            f"sbml_path='{args.sbml}'",
            f"reps={args.reps}",
            f"stop_time={args.t_end}",
            f"out_csv='{times_csv}'",
            f"load_csv='{load_csv}'",
            f"traj_csv='{traj_csv}'",
            f"apply_doses={'true' if apply_doses else 'false'}",
            f"run('{HERE / 'bench_matlab.m'}')",
        ]
        run([args.matlab, "-batch", "; ".join(cmd_parts)])
        return np.atleast_1d(np.loadtxt(times_csv)).tolist()

    matlab_inproc = matlab_inproc_run(
        "no-dosing", False,
        args.work / "matlab_times.csv",
        args.work / "matlab_load.csv",
        args.work / "matlab_traj.csv",
    )

    print(f"[cpp baseline] calibrating qsp_sim startup...")
    cpp_startup = calibrate_cpp_startup(qsp_sim)
    print(f"  startup baseline: {cpp_startup*1000:.1f} ms")

    # Binary output rather than CSV: SimBiology's `sbiosimulate` returns
    # trajectories in memory (no formatting cost), so timing the C++ side
    # against a CSV write would unfairly bill it for ~3-5 ms of float
    # formatting per call. Binary out is also what an SBI consumer would
    # use in production.
    def cpp_inproc_run(label, scenario_args, bin_path):
        print(f"[cpp in-process / {label}] {args.reps} reps...")
        cmd = [
            str(qsp_sim), "--param", str(param_xml),
            "--binary-out", str(bin_path),
            "--t-end-days", str(args.t_end),
            "--min-cadence-hours", str(args.dt * 24.0),
            # SimBiology's bare sbmlexport produces no listOfUnitDefinitions,
            # so the model's rate constants are in 1/day. Override the
            # binary's default 1/s assumption per invocation.
            "--time-unit", "days",
        ] + scenario_args
        wall = time_subprocess(cmd, args.reps, capture_output=True)
        return [max(t - cpp_startup, 0.0) for t in wall], cmd

    cpp_inproc, cpp_cmd_no_dose = cpp_inproc_run(
        "no-dosing", [], args.work / "cpp_out.bin",
    )

    # 2b. Dosed regime: 6 boluses at 30-day intervals starting day 30.
    matlab_inproc_dosed = matlab_inproc_run(
        "dosed", True,
        args.work / "matlab_times_dosed.csv",
        args.work / "matlab_load_dosed.csv",
        args.work / "matlab_traj_dosed.csv",
    )
    scenario_yaml = HERE / "scenario.yaml"
    drug_meta_yaml = HERE / "drug_metadata.yaml"
    cpp_inproc_dosed, _ = cpp_inproc_run(
        "dosed",
        ["--scenario", str(scenario_yaml),
         "--drug-metadata", str(drug_meta_yaml)],
        args.work / "cpp_out_dosed.bin",
    )

    # 3. Wall-clock cold-start timings.
    print(f"\n[matlab cold] {args.cold_reps} reps...")
    matlab_cold_cmd = "; ".join([
        f"sbml_path='{args.sbml}'",
        f"stop_time={args.t_end}",
        f"run('{HERE / 'bench_matlab_cold.m'}')",
    ])
    matlab_cold = time_subprocess(
        [args.matlab, "-batch", matlab_cold_cmd], args.cold_reps,
        capture_output=True,
    )

    print(f"[cpp cold] {args.cold_reps} reps...")
    cpp_cold = time_subprocess(cpp_cmd_no_dose, args.cold_reps,
                               capture_output=True)

    # 3b. Parity check (untimed). Run qsp_sim once with --csv-out for each
    # regime and diff against MATLAB's last trajectory.
    print("\n[parity] running qsp_sim with --csv-out for both regimes...")
    from qsp_codegen.parity import compare as parity_compare
    parity_results = []
    for label, scenario_args, matlab_traj in (
        ("no-dosing", [], args.work / "matlab_traj.csv"),
        ("dosed",
         ["--scenario", str(scenario_yaml),
          "--drug-metadata", str(drug_meta_yaml)],
         args.work / "matlab_traj_dosed.csv"),
    ):
        cpp_traj = args.work / f"cpp_traj_{label.replace('-', '_')}.csv"
        run([
            str(qsp_sim), "--param", str(param_xml),
            "--csv-out", str(cpp_traj),
            "--t-end-days", str(args.t_end),
            "--min-cadence-hours", str(args.dt * 24.0),
            "--time-unit", "days",
        ] + scenario_args, capture_output=True)
        passed, report = parity_compare(
            str(matlab_traj), str(cpp_traj),
            rtol=args.parity_rtol, atol=args.parity_atol,
        )
        parity_results.append((label, passed, report))
        print(f"\n--- parity / {label} ---")
        print(report)

    # 4. Write outputs.
    write_csv({
        "matlab_inproc": matlab_inproc,
        "cpp_inproc": cpp_inproc,
        "matlab_inproc_dosed": matlab_inproc_dosed,
        "cpp_inproc_dosed": cpp_inproc_dosed,
        "matlab_cold": matlab_cold,
        "cpp_cold": cpp_cold,
    }, args.out)
    write_table([
        ("Integration only, no dosing", matlab_inproc, cpp_inproc),
        ("Integration only, 6-bolus schedule", matlab_inproc_dosed, cpp_inproc_dosed),
        ("Wall-clock per invocation, no dosing", matlab_cold, cpp_cold),
    ], args.out_table)

    print(f"\nWrote {args.out}")
    print(f"Wrote {args.out_table}\n")
    print(args.out_table.read_text())

    if any(not ok for _, ok, _ in parity_results):
        sys.exit("\nParity check FAILED — speed numbers are not trustworthy.")
    print("[parity] all regimes passed.")


if __name__ == "__main__":
    main()
